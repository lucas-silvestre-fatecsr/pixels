                    <div class="text-justify">
                    
                        <p>Desenho vetorial é o uso de formas  primitivas geométricas como pontos, linhas, curvas e formas ou polígonos - todos os quais são baseados em expressões matemáticas - para representar imagens em computação gráfica.</p>
                        <p>Os desenhos vetoriais são baseados em vetores (também conhecidos como caminhos), que conduzem através de locais chamados pontos de controle ou nós.</p>
                        <p>Cada um desses pontos possui uma posição definida nos eixos x e y do plano de trabalho e determinam a direção do caminho.</p>
                        <p>Além disso, a cada caminho pode ser atribuído uma cor de traço, forma, espessura e preenchimento.</p>
                        <p>Estas propriedades não aumentam o tamanho dos arquivos de desenho vetorial de maneira substancial, uma vez que todas as informações residem na estrutura do documento, que apenas descreve como o vetor deve ser desenhado. </p>

